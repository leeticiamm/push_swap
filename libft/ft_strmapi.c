#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	char			*str;
	unsigned int	num;

	num = 0;
	if (!s || !f)
		return (0);
	str = malloc(sizeof(char) * ft_strlen(s) + 1);
	if (!str)
		return (0);
	while (s[num])
	{
		str[num] = f(num, s[num]);
		num++;
	}
	str[num] = '\0';
	return (str);
}
